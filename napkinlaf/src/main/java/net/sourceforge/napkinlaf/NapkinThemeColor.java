package net.sourceforge.napkinlaf;

/**
 * The types of colors used in Themes.
 *
 * @see NapkinTheme
 * @see NapkinTheme#getColor(NapkinThemeColor)
 */
public enum NapkinThemeColor {
    /** The default pen color. */
    PEN_COLOR,
    /** The check mark pen color. */
    CHECK_COLOR,
    /** The color to fill in selected radio buttons. */
    RADIO_COLOR,
    /** The color for highlighting. */
    HIGHLIGHT_COLOR,
    /** The rollover highlight color. */
    ROLLOVER_COLOR,
    /** The text color for the currently-selected item. */
    SELECTION_COLOR,
    /** The background color. */
    BACKGROUND_COLOR
}
