package net.sourceforge.napkinlaf;

/**
 * The specific themes that are known and used by the Look and Feel.  These are
 * generic names, which are mapped to specific themes.
 *
 * @see NapkinTheme#getTheme(NapkinKnownTheme)
 */
public enum NapkinKnownTheme {
    /** Index for the theme used for most windows. */
    BASIC_THEME,
    /** Index for the theme used for popup windows. */
    POPUP_THEME
}
